package estruturasCondicionais;

public class repeticaoDoWhile {

	public static void main(String[] args) {
		
		int valor = 1;
		
        do
        {
        	System.out.printf("%d ",valor);
        	++valor;
        }while(valor<= 20);//fim da instrução do ... while
		
        System.out.println();	
        	
	}

}
